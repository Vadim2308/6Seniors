/**
 * Ошибка в setTimeout
 * Что вы думаете? Выполнится ли .catch? Поясните свой ответ.
 */
{
    new Promise(function(resolve, reject) {
        setTimeout(() => {
            throw new Error("Whoops!");
            // Нет, catch не выполнится, т.к. мы просто через 1 секунду генерим ошибку, но не реджектим промис.
            // Неявным try {} catch обрабатываются только синхронные ошибки. А здесь ошибка генерируется позже
            // Если использовать вместо reject(new Error("Whoops!")), тогда все бы заработало
        }, 1000);
    }).catch(alert);
}